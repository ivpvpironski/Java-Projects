package moev_oop;

public class math {

    static double sum(int n1, double n2){
        double sum = n1 + n2;
        return sum;
    }

    static double minus(int n1,double n2){
        double minus = n1 - n2;
        return minus;
    }
    static double po(int n1, double n2){
        double po = n1 * n2;
        return po;
    }
    static double del(int n1, double n2){
        double del = n1/n2;
        return del;
    }

}
