class Calculator{

    static void addition(double a, double b){
        System.out.println(a + b);
    }
    static void subtraction(double a, double b){
        System.out.println(a - b);
    }
    static void multiplication(double a, double b){
        System.out.println(a * b);
    }
    static void division(double a, double b){
        System.out.println(a / b);
    }
}